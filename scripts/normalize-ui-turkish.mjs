function walk(entryPath, files) {
  const stat = fs.statSync(entryPath)

  if (stat.isFile()) {
    if (textExtensions.has(path.extname(entryPath)) && !entryPath.includes('-yabujin.')) {
      files.push(entryPath)
    }
    return
  }

  for (const entry of fs.readdirSync(entryPath, { withFileTypes: true })) {
    if (entry.name === 'node_modules') continue
    walk(path.join(entryPath, entry.name), files)
  }
}

const files = []
for (const targetPath of targetPaths) {
  if (fs.existsSync(targetPath)) {
    walk(targetPath, files)
  }
}

const changedFiles = []

for (const filePath of files) {
  const original = fs.readFileSync(filePath, 'utf8')
  let next = original

  for (const [pattern, replacement] of replacements) {
    next = next.replace(pattern, replacement)
  }

  if (next !== original) {
    fs.writeFileSync(filePath, next, 'utf8')
    changedFiles.push(path.relative(rootDir, filePath))
  }
}

if (!changedFiles.length) {
  console.log('UI Turkish normalization: no changes needed.')
  process.exit(0)
}

console.log('UI Turkish normalization updated files:')
for (const filePath of changedFiles) {
  console.log(`- ${filePath}`)
}
